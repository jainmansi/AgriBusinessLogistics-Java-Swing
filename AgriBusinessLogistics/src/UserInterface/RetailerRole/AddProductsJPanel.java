    private void addCatBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCatBtnActionPerformed
        Product p = (Product) productComboBox.getSelectedItem();
        if (p != null) {
            int i = 0;
            for (Product retProd : account.getProductCatalog().getProductList()) {
                if (retProd.getName().toLowerCase().equals(p.getName().toLowerCase())) {
                    ++i;
                }
            }
            if (i == 0) {
                try {
                    String batchNumber = batchTxtField.getText().trim();
                    String cost = costTxtField.getText().trim();
                    if (!batchNumber.isEmpty() && !cost.isEmpty()) {
                        Product newProd = new Product();
                        newProd.setName(p.getName());
                        newProd.setPrice(Integer.parseInt(cost));
                        account.getProductCatalog().getProductList().add(newProd);
                        //newProd.setName(p.getName());
                        //newProd.setPrice(p.getPrice());
                        InventoryItem ii = account.getInventory().addInventory();
                        ii.setProduct(p);
                        ii.setName(p.getName());
                    } else {
                        JOptionPane.showMessageDialog(null, "please enter appropriate values");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "please enter appropriate values");
                }
            } else {
                JOptionPane.showMessageDialog(null, "This product is already present in your catalog");
            }
        }
    }//GEN-LAST:event_addCatBtnActionPerformed

