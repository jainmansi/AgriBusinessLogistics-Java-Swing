    private void detailsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailsBtnActionPerformed
        int selectedRow = complaintsJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row first.");
            return;
        }

        CustomerWorkRequest request = (CustomerWorkRequest) complaintsJTable.getValueAt(selectedRow, 0);
        
        customerTxtField.setText(request.getSender().getPerson().getName());
        retailerTxtField.setText(request.getRfid().getRetailer().getPerson().getName());
        supplierTxtField.setText(request.getRfid().getSupplier().getPerson().getName());
        farmerTxtField.setText(request.getRfid().getFarmer().getPerson().getName());
        issueTxtField.setText(request.getIssue());
        resultTxtField.setText(request.getMessage());
        messageTxtField.setText(request.getFDAMessage());
    }//GEN-LAST:event_detailsBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed


