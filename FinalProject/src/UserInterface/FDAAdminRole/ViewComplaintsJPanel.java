    public void populateComplaintTable(){
        DefaultTableModel model = (DefaultTableModel) complaintsJTable.getModel();
        model.setRowCount(0);

        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {

            if (org instanceof FDAOrganization) {

                for (WorkRequest request : org.getWorkQueue().getWorkRequestList()) {
                    CustomerWorkRequest cusrequest = (CustomerWorkRequest) request;                    
                        Object[] row = new Object[6];
                        row[0] = cusrequest;
                        row[1] = cusrequest.getRfid();
                        row[2] = cusrequest.getRequestDate();
                        row[3] = cusrequest.getResolveDate();
                        row[4] = cusrequest.getReceiver();
                        row[5] = cusrequest.getStatus();
                        model.addRow(row);                                       
                }
            }
        }
    }

